app = MeetingAsrTyper(
    add_completion=False,
    add_help_option=False,
    context_settings=HELP_CONTEXT,
    invoke_without_command=True,
    no_args_is_help=False,
    pretty_exceptions_enable=False,
    help=ROOT_HELP,
)


def _version_callback(value: bool) -> None:
    """
    Print package version and exit when requested.

    Args:
        value: Whether the eager ``--version`` flag was passed.

    Returns:
        None.
    """
    if not value:
        return
    typer.echo(f"meeting-asr {_installed_version()}")
    raise typer.Exit()


def _installed_version() -> str:
    """
    Return the installed package version.

    Returns:
        Package version, or a local-development fallback.
    """
    try:
        return package_version("meeting-asr")
    except PackageNotFoundError:
        return "0.0.0+local"


@app.callback()
def root(
    ctx: typer.Context,
    version: bool = typer.Option(
        False,
        "--version",
        callback=_version_callback,
        is_eager=True,
        help="Show version and exit.",
    ),
    no_color: bool = typer.Option(
        False, "--no-color", help="Disable colored Rich output."
    ),
    verbose: bool = typer.Option(
        False, "--verbose", "-v", help="Enable verbose diagnostic logs."
    ),
    lang: str | None = typer.Option(
        None,
        "--lang",
        help="Help language: auto, en, or zh.",
        autocompletion=complete_cli_language,
    ),
    help_: bool = typer.Option(
        False, "--help", "-h", help="Show this message and exit."
    ),
) -> None:
    """Configure root command options."""
    try:
        configure_cli_language(lang)
    except ValueError as exc:
        raise click.BadParameter(str(exc), param_hint="--lang") from exc
    configure_cli_output(no_color=no_color, verbose=verbose)
    configure_logging(verbose=verbose)
    if help_ or ctx.invoked_subcommand is None:
        render_help(get_command(app), ())
        raise typer.Exit()


def help_command(ctx: typer.Context) -> None:
    """Show help for the root command or a nested command."""
    command_path = tuple(ctx.args)
    command = _resolve_help_command(get_command(app), command_path)
    render_help(command, command_path)


def _resolve_help_command(
    root_command: click.Command, command_path: tuple[str, ...]
) -> click.Command:
    """
    Resolve a nested command path for ``meeting-asr help``.

    Args:
        root_command: Root Click command generated by Typer.
        command_path: Nested command names.

    Returns:
        Click command whose help should be printed.
    """
    command = root_command
    for name in command_path:
        if not isinstance(command, click.Group):
            raise click.UsageError(
                f"`{' '.join(command_path)}` is not a command group."
            )
        child = command.commands.get(name)
        if child is None:
            raise click.UsageError(
                f"No such command for help: {' '.join(command_path)}"
            )
        command = child
    return command


