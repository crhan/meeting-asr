def main() -> None:
    """Run the root Typer app."""
    if agent.root_version_json_requested(sys.argv[1:]):
        from app.presentation.cli.json_output import emit_json

        emit_json(agent.version_envelope())
        return
    app()


if __name__ == "__main__":
    main()
